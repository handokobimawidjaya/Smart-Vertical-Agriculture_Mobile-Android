package com.example.hans.agrigo.MenuRegister.Model;

public class RegisterModel {
    private String email;

    private String username;

    private String password;

    private String name2;

    private String nomor;

    private String alamat;

    private String macSensor;

    private String macRelay;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName2() {
        return name2;
    }

    public void setName2(String name2) {
        this.name2 = name2;
    }

    public String getNomor() {
        return nomor;
    }

    public void setNomor(String nomor) {
        this.nomor = nomor;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getMacSensor() {
        return macSensor;
    }

    public void setMacSensor(String macSensor) {
        this.macSensor = macSensor;
    }

    public String getMacRelay() {
        return macRelay;
    }

    public void setMacRelay(String macRelay) {
        this.macRelay = macRelay;
    }
}
